def my_sum(a, b):
    """
    Sums two values together.
    :param a: first number to add
    :type a: int
    :param b: second number to add
    :type b: int
    :return: result of the sum
    """

    return a + b


# если навести курсором на my_sum(1, 2), будет
# видно наше описание функции, желательно делать
# описание функции, если с ней будут работать другие
my_sum(1, 2)
